// Helper to get animals by pen type and location
export const getAnimalsByPenAndLocation = (penType: string, location: string): Animal[] => {
  return ANIMALS.filter(animal => animal.penType === penType && animal.location === location);
};

// Helper to get an animal by ID
export const getAnimalById = (id: string): Animal | undefined => {
  return ANIMALS.find(animal => animal.id === id);
};

// Helper to get all animals for a location
export const getAnimalsByLocation = (location: string): Animal[] => {
  return ANIMALS.filter(animal => animal.location === location);
};
